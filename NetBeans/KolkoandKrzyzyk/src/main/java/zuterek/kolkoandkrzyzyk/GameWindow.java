    private void initGame(){
        player = DrawPlayer.randomPlayer();
        sign = DrawPlayer.randomPlayer();
        if(player){
            jLGracz1.setBackground(Color.GREEN);
            jLGracz2.setBackground(Color.BLUE);
        }else{
            jLGracz2.setBackground(Color.GREEN);
            jLGracz1.setBackground(Color.BLUE);
        }
        if(sign){
            jLZnakGracza1.setText("O");
            jLZnakGracza2.setText("X");
        }else{
            jLZnakGracza1.setText("X");
            jLZnakGracza2.setText("O");
        }
    }
    
